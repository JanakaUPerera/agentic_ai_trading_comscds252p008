def create_zip_from_bundle(
    bundle_dir: Path = BUNDLE_DIR,
    zip_file: Path = BUNDLE_ZIP_FILE,
) -> Path:
    """
    Zip the final analysis bundle.
    """
    if zip_file.exists():
        zip_file.unlink()

    with ZipFile(zip_file, "w", compression=ZIP_DEFLATED) as zip_handle:
        for file_path in bundle_dir.rglob("*"):
            if file_path.is_file():
                arcname = file_path.relative_to(bundle_dir)
                zip_handle.write(file_path, arcname=str(arcname))

    print(f"Created ZIP bundle: {zip_file}")
    return zip_file


def run_bundle_results_pipeline() -> Path:
    """
    Build the final analysis bundle and ZIP file.
    """
    print("Preparing final analysis bundle...")
    reset_bundle_directory()
    collect_bundle_files()
    zip_path = create_zip_from_bundle()
    print("Final analysis bundle completed successfully.")
    return zip_path


if __name__ == "__main__":
    try:
        run_bundle_results_pipeline()
    except Exception as e:
        print(f"Error running bundling pipeline: {e}")