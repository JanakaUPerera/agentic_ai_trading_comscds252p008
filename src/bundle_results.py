from __future__ import annotations

import shutil
from pathlib import Path
from zipfile import ZipFile, ZIP_DEFLATED

from src.config import BASE_DIR, OUTPUTS_DIR, REPORTS_DIR


BUNDLE_DIR = OUTPUTS_DIR / "final_analysis_bundle"
BUNDLE_ZIP_FILE = OUTPUTS_DIR / "final_analysis_bundle.zip"


def reset_bundle_directory(bundle_dir: Path = BUNDLE_DIR) -> None:
    """
    Remove and recreate the bundle directory.
    """
    if bundle_dir.exists():
        shutil.rmtree(bundle_dir)

    bundle_dir.mkdir(parents=True, exist_ok=True)


def copy_file_if_exists(source: Path, destination: Path) -> None:
    """
    Copy a file into the bundle if it exists.
    """
    if source.exists() and source.is_file():
        destination.parent.mkdir(parents=True, exist_ok=True)
        shutil.copy2(source, destination)
        print(f"Added to bundle: {source}")


def create_results_readme(bundle_dir: Path = BUNDLE_DIR) -> Path:
    """
    Create a simple README file explaining the bundle contents.
    """
    readme_path = bundle_dir / "README_results.txt"

    content = """Agentic AI Trading Workflow - Final Analysis Bundle

This bundle contains the main outputs generated by the cloud pipeline.

Folders:
- figures/: Main visual outputs for interpretation
- tables/: Summary tables used in reporting
- outputs/: Important pipeline result files

Suggested files to review first:
1. figures/strategy_vs_benchmark.png
2. figures/portfolio_growth.png
3. figures/crypto_asset_clusters.png
4. tables/backtest_summary.csv
5. outputs/decision_summary.csv
6. outputs/risk_summary.csv
7. outputs/crypto_news_sentiment_summary.csv
"""

    readme_path.write_text(content, encoding="utf-8")
    print(f"Created bundle README: {readme_path}")
    return readme_path


